"use client";
import { zodResolver } from "@hookform/resolvers/zod";
import { Form as CustomForm } from "@/src/components/form";
import { useForm } from "react-hook-form";
import z from "zod";
import { useAuthTranslations } from "@/src/features/auth/translations/hooks/useAuthTrans";
import { useSignUp } from "@/src/api";
import { LocalRegisterInputSchema as schema } from "@avuny/api/schemas";

export const SignUpForm = () => {
  const { authLabels, authErrorTranslations, authHeaderTranslations } =
    useAuthTranslations();

  const form = useForm<z.infer<typeof schema>>({
    resolver: zodResolver(schema),
  });
  const { mutate, isPending, error } = useSignUp();

  return (
    <>
      <CustomForm
        error={error}
        errorMap={authErrorTranslations}
        cardTitle={authHeaderTranslations("signUp")}
        fields={[
          {
            key: "name",
            content: {
              name: "identifier",
              type: "text",
            },

            spans: {
              base: 4,
              md: 2,
            },
          },
          {
            key: "description",
            content: {
              name: "name",
              type: "text",
            },

            spans: {
              base: 4,
              md: 2,
            },
          },
          {
            key: "password",
            content: {
              name: "password",
              type: "password",
            },

            spans: {
              base: 4,
              md: 4,
            },
          },
        ]}
        getLabel={authLabels}
        form={form}
        api={{ onSubmit: (data) => mutate({ data }), isLoading: isPending }}
      />
    </>
  );
};
